export function formatNewsDate(dateStr) {
  return new Date(dateStr).toLocaleDateString('en-KE', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  })
}

export function getNewsBySlug(slug) {
  return newsArticles.find((article) => article.id === slug)
}

export function getFeaturedArticle() {
  return newsArticles.find((article) => article.isFeatured) ?? newsArticles[0]
}

export function getNewsCategories() {
  return [...new Set(newsArticles.map((article) => article.category))]
}

export function getRelatedArticles(currentId, limit = 3) {
  const current = getNewsBySlug(currentId)
  if (!current) return []

  const sameCategory = newsArticles.filter(
    (article) => article.id !== currentId && article.category === current.category,
  )
  const others = newsArticles.filter(
    (article) => article.id !== currentId && article.category !== current.category,
  )

  return [...sameCategory, ...others].slice(0, limit)
}

export function estimateReadTime(article) {
  const text = article.body
    .map((block) => {
      if (block.type === 'list') return block.items.join(' ')
      return block.text ?? ''
    })
    .join(' ')
  const words = text.split(/\s+/).filter(Boolean).length
  return Math.max(1, Math.ceil(words / 200))
}
